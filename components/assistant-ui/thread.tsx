const ModelSelector: FC = () => {
  const { selectedModel, setSelectedModel } = useModel();

  const models = [
    { id: "groq/llama-3.1-70b", name: "Groq Llama 3.1 (Instant)" },
    { id: "google/gemini-2.5-flash", name: "Gemini 2.5 Flash (Smart & Fast)" },
    { id: "poolside/laguna-m.1:free", name: "Poolside Laguna (Coding)" },
  ];

  const currentModel = models.find((m) => m.id === selectedModel) || models[0];

  return (
    <DropdownMenu>
      <DropdownMenuTrigger asChild>
        <Button
          variant="ghost"
          size="sm"
          className="h-8 gap-2 rounded-lg px-2 text-muted-foreground hover:text-foreground"
        >
          <Cpu className="size-3.5" />
          <span className="text-xs font-medium">{currentModel.name}</span>
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="start" className="w-48">
        {models.map((model) => (
          <DropdownMenuItem
            key={model.id}
            onClick={() => setSelectedModel(model.id)}
            className="text-xs"
          >
            {model.name}
          </DropdownMenuItem>
        ))}
      </DropdownMenuContent>
    </DropdownMenu>
  );
};
